function print256Colors()
{
    for i in {0..255}; do echo -e "\e[38;05;${i}m38;05;${i}m"; done | column -c 50 -s ' '
    echo -e "\e[m"
}

function print8Colors()
{
    for i in {0..7}; do echo -e "\E[0;3${i}m 0;3${i} - \E[1;3${i}m 1;3${i}"; done | column -c 4 -s ' '
    echo -e "\e[m"
}

